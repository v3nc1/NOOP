
public class Stereo {

	public void on() {

		System.out.println("Turning on stereo...");
	}

	public void cdIn() {

		System.out.println("Setting a CD in stereo...");
	}

	public void volume() {

		System.out.println("Turning volume on: 10");
	}

	public void off() {

		System.out.println("Turning off stereo...");
	}

	public void cdEject() {

		System.out.println("Ejecting CD...");
	}

	public void volumeOff() {

		System.out.println("Volume set to 0...");
	}

}
