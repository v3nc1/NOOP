
public class SubSystemEnergy {

	public void on() {

		System.out.println("SubSystemEnergy state: ON");
	}

	public void off() {

		System.out.println("SubSystemEnergy state: OFF");
	}

	public void mode(String mode) {

		System.out.println("SubSystemEnergy mode: " + mode);
	}

	public void start() {

		System.out.println("SubSystemEnergy : START");
	}
	
	public void stop() {

		System.out.println("SubSystemEnergy : STOP");
	}

}
